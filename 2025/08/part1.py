#!/usr/bin/env python
from dataclasses import dataclass
from collections import defaultdict

CONNECTIONS = 1000

@dataclass(frozen=True)
class Point:
    x: int
    y: int
    z: int

    def distance_to(self, p: 'Point') -> int:
        dx = self.x - p.x
        dy = self.y - p.y
        dz = self.z - p.z
        return dx*dx + dy*dy + dz*dz


